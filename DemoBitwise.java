public class DemoBitwise {
	public static void main(string args []) {
		int x = 5, y = 6;
		system.out.println("x = " + x);
		system.out.println("y = " + y);
		system.out.println("x & y = " +(x & y));
		system.out.println("x | y = " +(x | y));
		system.out.println("x ^ y = " +(x ^ y));
		
		//komplemen 
		int z = 3;
		system.out.println("z = " + z);
		
		int a = ~z;
		system.out.println("a = " + a);
		
	}
}
		